namespace LogSharp
{
    /// <summary>
    /// Log severity levels.
    /// </summary>
    public enum LogSeverity
    {
        /// <summary>
        /// Trace severity level.
        /// </summary>
        TRACE,

        /// <summary>
        /// Info severity level.
        /// </summary>
        INFO,

        /// <summary>
        /// Debug severity level.
        /// </summary>
        DEBUG,

        /// <summary>
        /// Warning severity level.
        /// </summary>
        WARNING,

        /// <summary>
        /// Error severity level.
        /// </summary>
        ERROR,

        /// <summary>
        /// Fatal severity level.
        /// </summary>
        FATAL
    }
}